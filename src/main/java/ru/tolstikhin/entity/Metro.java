package ru.tolstikhin.entity;

public class Metro {
    private int id;
    private String name;

    public Metro() {
    }

    public Metro(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
